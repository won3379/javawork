package data;

public class PrintData {

	public static void main(String[] args) {
		// 데이터 다루기 -코드의 끝은 세미콜론(;)
		//숫자 (상수-정해진 수 , Constant Number
		System.out.println(10); //정수
		System.out.println(2.54);
		System.out.println(2.54+10); //덧셈
		System.out.println("null");
		
		System.out.println("==================");
		
		
		//문자 - 쌍따옴표로 감싼 데이터
		System.out.println("apple");//문자열
		System.out.println("A"); //한 문자인 경우는 홀따옴표로 감쌈
		System.out.println('가'); //한글한문자
		System.out.println("사과"); //문자열
	}

}
